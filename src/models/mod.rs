use serde::de::DeserializeOwned;
use serde::Serialize;
use surreal_simple_querybuilder::queries::QueryBuilderInjecter;
use surrealdb::opt::PatchOp;
use surrealdb::opt::QueryResult;
use surrealdb::sql::thing;

